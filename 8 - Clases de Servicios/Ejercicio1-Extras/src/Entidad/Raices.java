
package Entidad;

public class Raices {
    // Atributos
    public double A;
    public double B;
    public double C;

    public Raices(double A, double B, double C) {
        this.A = A;
        this.B = B;
        this.C = C;
    }

    public Raices() {
    }

    public double getA() {
        return A;
    }

    public double getB() {
        return B;
    }

    public double getC() {
        return C;
    }

    public void setA(double A) {
        this.A = A;
    }

    public void setB(double B) {
        this.B = B;
    }

    public void setC(double C) {
        this.C = C;
    }
    
    
}
